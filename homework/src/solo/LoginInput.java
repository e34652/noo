package solo;

public class LoginInput { //로그인시 입력된 ID PW
	String loginInputId;
	String loginInputPw;

	public LoginInput() {
		
		this.loginInputId = loginInputId;
		this.loginInputId = loginInputPw;
	}
	public LoginInput(String loginInputId, String loginInputPw) {
		
		this.loginInputId = loginInputId;
		this.loginInputId = loginInputPw;
	}
	public String getLoginInputId() {
		return loginInputId;
	}
	public void setLoginInputId(String loginInputId) {
		this.loginInputId = loginInputId;
	}
	public String getLoginInputPw() {
		return loginInputPw;
	}
	public void setLoginInputPw(String loginInputPw) {
		this.loginInputPw = loginInputPw;
	}

	
}